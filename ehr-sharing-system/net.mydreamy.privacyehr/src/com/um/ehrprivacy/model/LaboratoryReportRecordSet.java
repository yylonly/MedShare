package com.um.ehrprivacy.model;

public class LaboratoryReportRecordSet {
	
	private String HB;
	private String HBsAg;
	private String AntiHBs;
	private String HBeAg;
	private String AntiHBe;
	private String AntiHCV;
	private String VDRL;
	
	
	public String getHB() {
		return HB;
	}
	public void setHB(String hB) {
		HB = hB;
	}
	public String getHBsAg() {
		return HBsAg;
	}
	public void setHBsAg(String hBsAg) {
		HBsAg = hBsAg;
	}
	public String getAntiHBs() {
		return AntiHBs;
	}
	public void setAntiHBs(String antiHBs) {
		AntiHBs = antiHBs;
	}
	public String getHBeAg() {
		return HBeAg;
	}
	public void setHBeAg(String hBeAg) {
		HBeAg = hBeAg;
	}
	public String getAntiHBe() {
		return AntiHBe;
	}
	public void setAntiHBe(String antiHBe) {
		AntiHBe = antiHBe;
	}
	public String getAntiHCV() {
		return AntiHCV;
	}
	public void setAntiHCV(String antiHCV) {
		AntiHCV = antiHCV;
	}
	public String getVDRL() {
		return VDRL;
	}
	public void setVDRL(String vDRL) {
		VDRL = vDRL;
	}
}
