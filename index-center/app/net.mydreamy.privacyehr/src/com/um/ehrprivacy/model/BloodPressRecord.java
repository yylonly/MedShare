package com.um.ehrprivacy.model;

public class BloodPressRecord {
	
	private String HighPress;
	private String LowPress;
	private String Pulse;
	private String TourNurse;
	public String getHighPress() {
		return HighPress;
	}
	public void setHighPress(String highPress) {
		HighPress = highPress;
	}
	public String getLowPress() {
		return LowPress;
	}
	public void setLowPress(String lowPress) {
		LowPress = lowPress;
	}
	public String getPulse() {
		return Pulse;
	}
	public void setPulse(String pulse) {
		Pulse = pulse;
	}
	public String getTourNurse() {
		return TourNurse;
	}
	public void setTourNurse(String tourNurse) {
		TourNurse = tourNurse;
	}
	
}
